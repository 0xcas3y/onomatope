// 读取现有 cards.json
const CARDS_PATH = path.join(__dirname, 'data/cards.json');
const data = JSON.parse(fs.readFileSync(CARDS_PATH, 'utf8'));
const existing = new Set(data.cards.map(c => c.word));
let nextId = Math.max(...data.cards.map(c => c.id)) + 1;

for (const gyou of Object.keys(REMAINING)) {
  for (const [word, meanings, mnemonic, collocations] of REMAINING[gyou]) {
    if (existing.has(word)) { continue; }
    data.cards.push({
      id: nextId++,
      gyou,
      word,
      kana: word,
      page: '',
      meanings,
      mnemonic,
      collocations,
      examples: []  // 会被 fetch-media.js 补齐
    });
  }
}

data.total = data.cards.length;
fs.writeFileSync(CARDS_PATH, JSON.stringify(data, null, 2));
console.log(`✅ Total cards: ${data.total}`);
console.log(`   By 行:`);
const byGyou = {};
for (const c of data.cards) byGyou[c.gyou] = (byGyou[c.gyou] || 0) + 1;
for (const [g, n] of Object.entries(byGyou).sort()) console.log(`     ${g}行: ${n}`);
